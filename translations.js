let currentLanguage = 'it';

// Imposta la lingua al caricamento
document.addEventListener('DOMContentLoaded', function() {
    setLanguage('it');
    
    // Aggiungi event listeners ai pulsanti di lingua
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            setLanguage(this.dataset.lang);
        });
    });
});

function setLanguage(lang) {
    currentLanguage = lang;
    
    // Aggiorna tutti gli elementi con data-i18n
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.dataset.i18n;
        const text = translations[lang][key];
        
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = text;
        } else {
            element.innerHTML = text;
        }
    });
    
    // Aggiorna i placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.dataset.i18nPlaceholder;
        element.placeholder = translations[lang][key];
    });
    
    // Aggiorna i bottoni di lingua (active state)
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });
    
    // Cambia la lingua dell'HTML
    document.documentElement.lang = lang === 'pt-BR' ? 'pt-BR' : lang;
    
    // Salva la preferenza nel localStorage
    localStorage.setItem('preferredLanguage', lang);
}

function t(key) {
    return translations[currentLanguage][key] || translations['it'][key] || key;
}